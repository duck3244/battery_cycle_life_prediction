<script setup>
import { onMounted, ref, watch } from 'vue'
import { fetchPlots, fetchReport, plotUrl } from '../api.js'

const props = defineProps({
  modelAvailable: { type: Boolean, default: false },
})

const plots = ref([])
const report = ref(null)
const error = ref(null)

async function load() {
  if (!props.modelAvailable) {
    plots.value = []
    report.value = null
    return
  }
  try {
    plots.value = await fetchPlots()
  } catch (err) {
    error.value = err.message
  }
  try {
    report.value = await fetchReport()
  } catch {
    report.value = null
  }
}

onMounted(load)
watch(() => props.modelAvailable, load)
</script>

<template>
  <section class="card">
    <h2>Saved results</h2>
    <p class="muted">Artifacts generated by the most recent training run.</p>

    <p v-if="!modelAvailable" class="muted">No model yet — train first to see plots.</p>

    <div v-else>
      <div v-if="report?.metrics" class="metrics">
        <div v-for="(value, key) in report.metrics" :key="key" class="metric">
          <span class="muted">{{ key }}</span>
          <strong>{{ typeof value === 'number' ? value.toFixed(4) : value }}</strong>
        </div>
      </div>

      <h3 v-if="plots.length">Plots</h3>
      <div class="plot-grid">
        <figure v-for="name in plots" :key="name">
          <img :src="plotUrl(name)" :alt="name" loading="lazy" />
          <figcaption class="muted">{{ name }}</figcaption>
        </figure>
      </div>
      <p v-if="error" class="pill err">{{ error }}</p>
    </div>
  </section>
</template>

